"""
Operadores de Comparação (Relacionais)
OP...... Significado.........Exemplo
 >          maior             2 > 1
 >=      maior ou igual       2 >= 2
 <          menor             1 < 2
 <=      menor ou igual       2 <= 2
 ==         igual             1 == 1
 !=         Diferente         1 != 0
 """

maior = 2 > 1
maior_ou_igual = 2 >= 2
menor = 1 < 2
menor_ou_igual = 1 <= 1
igual = 'a' == 'a'
diferente = 'b' != 'a'

print(f'{maior} ,{maior_ou_igual} ,{menor} ,{menor_ou_igual} ,{igual} ,{diferente}')

